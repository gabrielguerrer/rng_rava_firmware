/*
The RNG module is the core of the RAVA architecture, responsible for entropy generation and its
delivery to the host device. The exact mechanisms used to generate pulse counts, bits, and bytes
depend on the underlying hardware platform and must be implemented by the application through the
functions `rng_gen_pulse_count()`, `rng_gen_bit()`, and `rng_gen_byte()`.

In addition to single-request generation, the module also supports continuous byte-stream
transmissions. In this mode, the application must implement the `rng_start_byte_stream()`
functionality, typically using a hardware timer interrupt to trigger the generation of random bytes
at fixed intervals.

The general functionalities provided by this module include:

- Configuration of the RNG device, consisting of the PWM boost frequency and duty cycle parameters,
and the sampling interval used to count rising-edge pulses generated by the comparators monitoring
the avalanche noise sources

- Optional post-processing of generated random bytes using XOR-based methods

- Generation and transmission of pulse counts, random bits, random bytes, uniformly distributed
integers and floating-point values.

Implementation-specific functionality must provide the hooks declared in the "Implemented by the
application" section.
*/

