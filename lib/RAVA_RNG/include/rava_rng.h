/* ===========================
 * RAVA RNG
 * =========================== */

// Maximum number of random bytes accepted per generation request. This value affects the maximum
// time the device may remain busy processing a single request before handling other commands or
// interrupts.
#ifndef RNG_GEN_MAX_NBYTES_PER_CORE
#define RNG_GEN_MAX_NBYTES_PER_CORE 1024
#endif

// Runtime configuration of the RNG device
typedef struct rng_config_t
{
  pwm_boost_config_t pwm_boost;
  uint8_t sampling_interval;
} rng_config_t;

// Configuration and runtime state for fixed-interval random-byte streaming
typedef struct rng_byte_stream_t
{
  uint16_t req_id;
  comm_interface_t *comm;
  uint16_t n_bytes, interval_ms, interval_ms_max;
  uint8_t postproc_id, rng_cores;
  volatile bool streaming;
  volatile bool triggered; // Used in (ISR) TIMER3_COMPA_vect
} rng_byte_stream_t;

extern rng_config_t rng_cfg;
extern rng_byte_stream_t rng_byte_stream_cfg;

enum RNG_CORES
{                         // Output considering the bytes of cores A and B
  RNG_CORES_AB_DUAL,      // A,A... and B,B...
  RNG_CORES_AB_ALT,       // Alternating bytes: A,B,A,B...
  RNG_CORES_AB_XOR,       // XORed bytes: A^B,A^B...
  RNG_CORES_A,            // A,A...
  RNG_CORES_B,            // B,B...
  RNG_CORES_ENUM_LAST
};

enum RNG_BYTE_POST_PROCESSING
{
  RNG_PP_NONE,
  RNG_PP_XOR,
  RNG_PP_XOR_DICHTL,
  RNG_PP_ENUM_LAST
};

bool rng_setup(uint8_t sampling_interval);
uint8_t rng_get_sampling_interval(void);

void rng_write_byte_stream(void);

// COMM
void comm_rng_get_config(comm_interface_t *const comm);
void comm_rng_set_config(comm_interface_t *const comm);
void comm_rng_set_timing_debug(comm_interface_t *const comm);
void comm_rng_gen_pulse_counts(comm_interface_t *const comm);
void comm_rng_gen_bit(comm_interface_t *const comm);
void comm_rng_gen_bytes(comm_interface_t *const comm);
void comm_rng_gen_int8s(comm_interface_t *const comm);
void comm_rng_gen_int16s(comm_interface_t *const comm);
void comm_rng_gen_floats(comm_interface_t *const comm);
void comm_rng_gen_floats_downey(comm_interface_t *const comm);
void comm_rng_start_byte_stream(comm_interface_t *const comm);
void comm_rng_stop_byte_stream(comm_interface_t *const comm);
void comm_rng_get_status_byte_stream(comm_interface_t *const comm);

