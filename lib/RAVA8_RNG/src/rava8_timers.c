/*
Configures the internal PLL to generate a 48 MHz clock.

This configuration is required by peripherals such as Timer 4 and the USB subsystem.
*/
void pll_setup_48mhz(void)
{
  // Reset registers
  PLLCSR = 0;
  PLLFRQ = 0;

  // 16MHz clock
  #if F_CPU == 16000000UL
  PLLCSR |= _BV(PINDIV); //16 MHz clock source
  PLLFRQ |= _BV(PDIV3) | _BV(PDIV1);  // CLK_PLL 96MHz
  PLLFRQ |= _BV(PLLTM1) | _BV(PLLTM0); // PLLTM = CLK_PLL / 2
  PLLFRQ |= _BV(PLLUSB); // PLLUSB = CLK_PLL / 2

    // 8MHz clock
  #elif F_CPU == 8000000UL
  PLLFRQ |= _BV(PDIV2); // CLK_PLL 48MHz
  PLLFRQ |= _BV(PLLTM0); // PLLTM / 1
  #endif

  // Enable PLL
  PLLCSR |= _BV(PLLE);

  // Wait until PLL locks to the configured frequency
  while (!(PLLCSR & _BV(PLOCK))) {};
}

/*
Restores Timer 0 to its default inactive configuration.
*/
void timer0_reset(void)
{
  // Normal mode, TOP at 0xff
  TCCR0A = 0;

  // No clock source
  TCCR0B = 0;

  // Interrupts disabled
  TIMSK0 = 0;
}

/*
Configures Timer 0 to operate as a counter driven by external rising-edge pulses.

This mode is employed by the RNG module to count comparator pulses associated with randomness
channel "B".
*/
void timer0_setup_rng(void)
{
  // Reset
  timer0_reset();

  // External clock source on T0 pin
  TCCR0B = TIMER013_CLK_EXTERN_RISING_EDGE;
}

